	  <h4 class="title-divider"><span >Stress Echocardiogram</span></h4>
	  <strong><em>What is Stress Echocardiogram?</em></strong><br/>
	    Although Echocardiogram is a brilliant test, it looks at your heart while you are resting. As mentiones before this is good to look at the valves and chambers of the heart to see they are normal. <br/>
	    But there are times that we want to see how your heart muscle performs under stress. Stress here means when your heart needs to work harder and faster by walking, jogging or running. Under theses conditions your Cardiologist will closely look at the heart muscle straight after stress to make sure blood supply is adequate to all parts of the heart. If one section of the haert is not contracting as the others, that might mean that there is a narrowing in the artery supplying blood to that area, the precursor of Heart Attack.<br/>
	    The other benefit of Stess Echo is that during the test we continuously record your heart rate and rhythm and see how it is working under stress. Apart from echocardiogram of the test we get very useful information via these recordings.<br/>
	    And of course one of the most useful aspects of this test is to see how fit you are and how much stress you can tolerate. <br/> 
	  <strong><em>What does it involve?</em></strong><br/>
	    Here the difference is that first we need to get your heart going fast. We will ask you to jog on a treadmill. It starts at a very slow speed and every 3 minutes goes faster with more incline. At the end of the exercise, depending how much you can do, we will ask you to lie flat straight away on the bed and will perform Echocardiogram while your heart is beating fast and strong. <br/>Here you can see an image of a probe.<br/> 

